package utlity;

public class Lista {
		String head;
		String cont;
		
		public Lista(String head, String cont) {
			super();
			this.head = head;
			this.cont = cont;
		}

		public String getHead() {
			return head;
		}

		public void setHead(String head) {
			this.head = head;
		}

		public String getCont() {
			return cont;
		}

		public void setCont(String cont) {
			this.cont = cont;
		}
}
